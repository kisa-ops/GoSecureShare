  info "  Pulling ${img}:${TARGET_VERSION}..."
  if ! docker pull "${REGISTRY}/${NAMESPACE}/${img}:${TARGET_VERSION}"; then
    warn "  Failed to pull ${img}:${TARGET_VERSION}"
    _pull_failed=true
